class extrainfo(models.Model):
    user=models.OneToOneField(User,null=True,on_delete=models.CASCADE)
    gender=models.IntegerField(choices=gender,default=1)
    Nationality=models.IntegerField(choices=country,default=1)
    dayofbirth=models.IntegerField(choices=day,default=1)
    monthofbirth=models.IntegerField(choices=month,default=1)
    yearofbirth=models.CharField(max_length=4,null=True)
    def __str__(self):
        return str(self.user)
    
STATUS = (
    (0,"Draft"),
    (1,"Publish")
)

class Post(models.Model):
    title = models.CharField(max_length=200,)
    author = models.ForeignKey(User,on_delete= models.SET_NULL,null=True)
    updated_on = models.DateTimeField(auto_now= True)
    content = models.TextField()
    created_on = models.DateTimeField(auto_now_add=True)
    status = models.IntegerField(choices=STATUS, default=0)
    productimage=models.ImageField(null=True,blank=True,default='images/product.jpg',upload_to="images/")
    class Meta:
        ordering = ['-created_on']

    def __str__(self):
        return self.title